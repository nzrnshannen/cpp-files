/*
    Write a program to print the sum of two numbers.
*/

#include<iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout << "Enter num1: ";
    cin >> num1;
    cout << "Enter num2: ";
    cin >> num2;

    cout << "The sum of " << num1 << " and " << num2 << " is " << num1+num2 << endl;
}

